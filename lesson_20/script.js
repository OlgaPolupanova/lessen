
// безконечный цикл for
for ( ; true; ) {
  console.log('Hello world')
}
// цикл for который никогда не выполнится
for ( ; false; ) {
  console.log('Hello world')
}

// цикл for
for (var x = 0; x < 10 ; x++) {
  console.log('Hello world')
}

// проход по массиву
const colors = ['red', 'green', 'blue'];
for (var y = 0; y < colors.length; y++) {
  console.log(colors[y]);
}

